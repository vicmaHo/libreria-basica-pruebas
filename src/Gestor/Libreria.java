    public void actualizarTabla(){
        // extraigo los datos de la base de datos y los asigno a la tabla
        Connection conn = getConnection();
        PreparedStatement ps = null;
        ResultSet rs = null;
        
        try {
            ps = conn.prepareStatement(GETALL);
            rs = ps.executeQuery();
            while(rs.next()){
                Object[] datos = new Object[4];
                datos[0] = rs.getInt("id");
                datos[1] = rs.getString("titulo");
                datos[2] = rs.getString("autor");
                datos[3] = rs.getString("genero");
            
                modelo.addRow(datos);
            }
            jtDatos.setModel(modelo);
            // cierro todo lo referente a la conexion
            rs.close();
            ps.close();
            conn.close();
        } catch (Exception e) {
        }
        
    }
    
    public void limpiarCamposDatos(){
        txtAutor.setText("");
        txtGenero.setText("");
        txtId.setText("");
        txtTitulo.setText("");
    }
    
    public void limpiarTabla(){
        int numFilas = this.jtDatos.getRowCount();
        for(int i = numFilas - 1; i >= 0; i--){
            modelo.removeRow(i);
        }
    
    }
    
