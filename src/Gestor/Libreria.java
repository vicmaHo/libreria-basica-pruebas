    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection conn = getConnection(); // getconecction nos devuelve la conexion
        PreparedStatement ps = null;
        try {
            ps = conn.prepareStatement(INSERT);
            ps.setString(1, txtTitulo.getText());
            ps.setString(2, txtAutor.getText());
            ps.setString(3, txtGenero.getText());
            // ejecuto la instruccion, no es una query es un uptade
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this,
                    "Libro " + txtTitulo.getText() + " agregado correctamente.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error de conexion.");
        } finally {
            try {
                conn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error al cerrar conexion.");
            }
        }
        limpiarTabla();
        actualizarTabla();
        limpiarCamposDatos();
    }//GEN-LAST:event_btnGuardarActionPerformed

